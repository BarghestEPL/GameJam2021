class GraphicEngine:
    def __init__(self, win):
        self.win = win
        self.rad = 30
        self.gun_w = 5
        self.tile_size = 32
        self.colors = [
            "#7f5539",  # Background
            "#495057",  # Walls
            "#fcf6bd",  # Vote counts
            "#f94144",  # Red spawn
            "#48bfe3",  # Blue spawn
        ]

    def render_soldier(self, soldier, color):
        a = pg.Vector2(soldier["pos"])
        if soldier["selected"]:
            pg.draw.circle(self.win, (255, 255, 0), a, self.rad + 2)
        pg.draw.circle(self.win, color, a, self.rad)

        b = pg.Vector2(soldier["aim"])
        c = a - (a - b).normalize() * (self.rad + self.gun_w)
        pg.draw.line(self.win, (0, 0, 0), a, c, 8)

    def render(self, data):
        background_color = "#7f5539"
        self.win.fill(background_color)

        tile_size = 32
        tile_surface = pg.Surface((tile_size, tile_size))
        for i in range(len(arena)):
            for j in range(len(arena[0])):
                tile_surface.fill(self.colors[arena[i, j]])
                self.win.blit(tile_surface, (j * tile_size, i * tile_size))

        for soldier in data['pb']['soldiers']:
            self.render_soldier(soldier, (0, 0, 255))

        for soldier in data['pr']['soldiers']:
            self.render_soldier(soldier, (255, 0, 0))
