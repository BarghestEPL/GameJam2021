class Player:
    def __init__(self, sock, first):
        self.sock = sock

        if first:
            self.soldiers = [
                Soldier(pg.Vector2(100, 700)),
                Soldier(pg.Vector2(100, 100)),
                Soldier(pg.Vector2(200, 400)),
            ]
        else:
            self.soldiers = [
                Soldier(pg.Vector2(1180, 700)),
                Soldier(pg.Vector2(1180, 100)),
                Soldier(pg.Vector2(1000, 400)),
            ]

        self.target = self.soldiers[0]
        self.target.selected = True

    def update(self, dt):
        data = recv_msg(self.sock)
        if data is not None:
            if data["left"]:
                for soldier in self.soldiers:
                    if soldier.is_target(data["pos"]):
                        self.target.selected = False
                        self.target = soldier
                        soldier.selected = True
                        break
                else:
                    self.target.move_to(data)
            self.target.aim = data['pos']
            for soldier in self.soldiers:
                soldier.update(dt)

    def get_state(self):
        return {"soldiers": [soldier.get_state() for soldier in self.soldiers]}


class Soldier:
    def __init__(self, pos: pg.Vector2):
        self.aim = (0, 0)
        self.rad = 30
        self.pos = pos
        self.angle = 0
        self.health = 100
        self.speed = 200
        self.m_at = self.pos
        self.selected = False
        self.step = pg.Vector2(0, 0)

    def is_target(self, center):
        return self.pos.distance_to(center) < self.rad

    def move_to(self, data):
        self.m_at = pg.Vector2(data['pos'])
        self.step = (self.m_at - self.pos).normalize() * self.speed

    def update(self, dt):
        if self.pos.distance_to(self.m_at) > 5:
            tmp = self.pos + self.step * dt
            if bloc[int(tmp.y / 32), int(tmp.x / 32)] != 1:
                self.pos = tmp

    def get_state(self):
        return {
            "pos": tuple(self.pos),
            "aim": self.aim,
            "health": self.health,
            "selected": self.selected
        }
